run_opt(1) :- 
	findall(X,patient(X),List), findall(X, (member(Y,List), symptom(X,Y)), PossibleDiseases ),
	%write(PossibleDiseases), nl,
	%sort(D,X), nl,
	printlist(PossibleDiseases).
	%write(X), main.   	

run_opt(2) :- 
	findall(X, symptom(_,X),X),
	sort(X,D), nl, 
	printlist(D), nl,
	write('All symptoms displayed'), nl.
	%main.

run_opt(3) :- write('Opt 3'), 
	findall(X, symptom(_,X),X),
	sort(X,D), nl, 
	printlist(D), nl,
	write('Add a symptom from a the this list above followed by  period:'), nl,
	write('example fever.'),nl, nl,
	write('symptom: '),
	read(User_Symptom),
	assertz(patient(User_Symptom)),
	main.

run_opt(4) :- write('Finding Tests for symptoms'), nl, 
				findall(X,patient(X),List), findall(X, (member(Y,List), symptom(X,Y)), PossibleDiseases ),
				findall(X, (member(TestOptions, PossibleDiseases), test(TestOptions,X)), TestReturn),
				sort(TestReturn,D), nl, 
				printlist(D),
				write('Run a test from the list above: '), 
				read(User_test),
				assertz(testTaken(User_test)),
				main.
run_opt(5) :- write('Treatments based on test results'),nl,
	testTaken(TestTaken),
	findall(X2,testTaken(X2),List2),
	findall(Z, test(Z,TestTaken), Z),
	findall(Q,patient(Q),List), findall(Q, (member(Y,List), symptom(Q,Y)), PossibleDiseases ),
	intersection(PossibleDiseases, Z, CommonElements),
	write('Given the test(s): '), printlist(List2),nl,
	write('we can discover the treatments for these possible illnesses the patient has:'), nl, 
	printlist(CommonElements),nl,
	findall(X1, (member(Y1, CommonElements), treatment(Y1, X1)), Xs),
	write('Treatments: '), nl, 
	printlist(Xs).

run_opt(6) :- write('Resting symptoms/tests selected'), nl,
			retractall(patient(_)), retractall(testTaken(_))	, main. 
run_opt(7) :- write('Goodbye'), nl, halt. 

run_opt(_) :- write('Invalid option'), nl, halt.  

